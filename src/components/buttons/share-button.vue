<template>
  <v-list-tile 
    v-if="inList" 
    ripple 
    @click.stop="share"
  >
    <v-list-tile-title>{{ btnTxt }}</v-list-tile-title>
  </v-list-tile>

  <v-tooltip 
    v-else 
    top
  >
    <v-btn 
      slot="activator" 
      :color="color" 
      icon 
      @click.stop="share"
    >
      <v-icon>share</v-icon>
    </v-btn>
    <span>{{ btnTxt }}</span>
  </v-tooltip>
</template>
<script>
// /* eslint-disable */
import deleteButton from '@/components/buttons/delete-button'
export default {
  name: 'ShareButton',
  props: {
    url: {
      type: String,
      default: ''
    },
    song: {
      type: [Object],
      default() {
        return []
      }
    },
    inList: {
      type: Boolean,
      default: false
    }
  },
  data () {
    return {
      color: ''
    }
  },
  components: {
    'delete-button': deleteButton
  },
  computed : {
    surl () {
      return 'https://dreamcloud.netlify.com/#/t/' + this.song.source + '/' + encodeURIComponent(this.song.artist) + '/' + this.song.trackID
    },
    btnTxt () {
      return this.$vuetify.breakpoint.smAndDown ? 'Share' : 'Copy to clipboard'
    }
  },
  methods: {
    share () {
      this.$emit('clicked')
      this.color = 'primary'
      if(this.$UTILS.share(this.surl, this.song)) {
        this.$store.commit('snack', { b: true, c:'primary', s:'Link copied to clipboard' })
      } else {
        this.$store.commit('snack', { b: true, c:'primary', s:'Opening share menu' })
      }
      setTimeout(() => {
        this.color = ''
      }, 2000)
    }
  }
}
</script>

